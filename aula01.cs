namespace variaveis;

public class aula01
{
    public static void variaveis()
    {
        Console.WriteLine("Variáveis");

        string myName = "Hiago";
        Console.WriteLine(myName);

        int number = 10;
        Console.WriteLine(number);

        double numberDouble = 10.55;
        Console.WriteLine(numberDouble);

        char character = 'A';
        Console.WriteLine(character);

        bool boolean = true;
        Console.WriteLine(boolean);

        //Char representa um único caractere, string uma sequência.
    }
}